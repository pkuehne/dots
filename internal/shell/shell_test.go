func TestCleanRemovesStale(t *testing.T) {
	dir := t.TempDir()
	stale := filepath.Join(dir, "050-oldtool.sh")
	_ = os.WriteFile(stale, []byte("# stale"), 0o644)
	_ = os.WriteFile(filepath.Join(dir, "010-env.sh"), []byte("# env"), 0o644)

	cfg := config.Config{
		Shell:    config.ShellConfig{Dir: dir},
		Tools:    nil,
		RepoRoot: t.TempDir(),
	}
	if err := Clean(cfg, false); err != nil {
		t.Fatal(err)
	}
	if _, err := os.Stat(stale); !os.IsNotExist(err) {
		t.Error("stale snippet should have been removed")
	}
	if _, err := os.Stat(filepath.Join(dir, "010-env.sh")); err != nil {
		t.Error("010-env.sh should be kept")
	}
}

func TestAssembled(t *testing.T) {
	t.Setenv("HOME", t.TempDir())
	dir := t.TempDir()
	_ = os.WriteFile(filepath.Join(dir, "010-env.sh"), []byte("# env\n"), 0o644)
	_ = os.WriteFile(filepath.Join(dir, "020-path.sh"), []byte("# path\n"), 0o644)

	cfg := config.Config{Shell: config.ShellConfig{Dir: dir}}
	result, err := Assembled(cfg)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(result, "# env") || !strings.Contains(result, "# path") {
		t.Error("assembled output should contain both snippet contents")
	}
}
