// ── InsertBlock / RemoveBlock ─────────────────────────────────────────────────

func TestInsertBlockCreatesFile(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "sub", ".zshrc")

	modified, err := InsertBlock(path, zshBootstrapper, false)
	if err != nil {
		t.Fatal(err)
	}
	if !modified {
		t.Error("expected modified=true for new file")
	}
	data, _ := os.ReadFile(path)
	if !strings.Contains(string(data), MarkerStart) {
		t.Error("written file should contain marker start")
	}
}

func TestInsertBlockAppendsToExisting(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, ".zshrc")
	_ = os.WriteFile(path, []byte("# existing content\n"), 0o644)

	_, err := InsertBlock(path, zshBootstrapper, false)
	if err != nil {
		t.Fatal(err)
	}
	data, _ := os.ReadFile(path)
	text := string(data)
	if !strings.Contains(text, "# existing content") {
		t.Error("existing content should be preserved")
	}
	if !strings.Contains(text, MarkerStart) {
		t.Error("marker block should be appended")
	}
}

func TestInsertBlockUpdatesExisting(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, ".zshrc")
	initial := "# before\n" + zshBootstrapper + "\n# after\n"
	_ = os.WriteFile(path, []byte(initial), 0o644)

	newBlock := MarkerStart + "\n# updated\n" + MarkerEnd
	modified, err := InsertBlock(path, newBlock, false)
	if err != nil {
		t.Fatal(err)
	}
	if !modified {
		t.Error("expected modified=true when block content changed")
	}
	data, _ := os.ReadFile(path)
	text := string(data)
	if !strings.Contains(text, "# updated") {
		t.Error("block should be replaced with new content")
	}
	if !strings.Contains(text, "# before") || !strings.Contains(text, "# after") {
		t.Error("surrounding content should be preserved")
	}
}

func TestInsertBlockIdempotent(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, ".zshrc")

	_, _ = InsertBlock(path, zshBootstrapper, false)
	_, _ = InsertBlock(path, zshBootstrapper, false)

	data, _ := os.ReadFile(path)
	text := string(data)
	if strings.Count(text, MarkerStart) != 1 {
		t.Errorf("marker should appear exactly once after two inserts, got %d", strings.Count(text, MarkerStart))
	}
}

