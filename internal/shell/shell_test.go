// ── GenerateEnvSnippet ────────────────────────────────────────────────────────

func TestEnvSnippetFormat(t *testing.T) {
	cfg := config.Config{
		Env: config.EnvConfig{
			Vars: map[string]string{"EDITOR": "nvim", "PAGER": "less"},
		},
	}
	result := GenerateEnvSnippet(cfg)
	if !strings.Contains(result, "# Generated by dots") {
		t.Error("missing generated header")
	}
	if !strings.Contains(result, `export EDITOR="nvim"`) {
		t.Error("missing EDITOR export")
	}
	if !strings.Contains(result, `export PAGER="less"`) {
		t.Error("missing PAGER export")
	}
}

