func TestGenerateConfig_DeltaOmittedWhenMissing(t *testing.T) {
	// Skip if delta happens to be installed — we can't control exec.LookPath.
	if _, err := exec.LookPath("delta"); err == nil {
		t.Skip("delta is installed; cannot test absent-delta path")
	}
	tool := config.Tool{Name: "delta"}
	tool.Git.Pager = true
	tool.Git.Diff = true
	cfg := config.Config{}
	cfg.Git.DefaultBranch = "main"
	cfg.Tools = []config.Tool{tool}
	result := GenerateConfig(cfg)
	if strings.Contains(result, "pager = delta") {
		t.Error("expected delta pager to be absent when delta not installed")
	}
	if strings.Contains(result, "[diff]") {
		t.Error("expected [diff] to be absent when delta not installed")
	}
}

