func TestWriteManaged_DryRun(t *testing.T) {
	cfg := config.Config{}
	cfg.Git.DefaultBranch = "main"
	// Dry run should not create any files.
	t.Setenv("HOME", t.TempDir())
	if err := WriteManaged(cfg, true, false, nil); err != nil {
		t.Fatalf("WriteManaged dry-run: %v", err)
	}
}

func TestWriteManaged_CreatesFile(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	cfg := config.Config{}
	cfg.Git.Name = "Alice"
	cfg.Git.DefaultBranch = "main"

	if err := WriteManaged(cfg, false, false, nil); err != nil {
		t.Fatalf("WriteManaged: %v", err)
	}

	outPath := filepath.Join(home, ".config", "dots", "git", "managed.gitconfig")
	data, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("reading managed.gitconfig: %v", err)
	}
	if !strings.Contains(string(data), "name = Alice") {
		t.Error("managed.gitconfig missing name")
	}
}

func TestWriteManaged_InsertsIncludeBlock(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	cfg := config.Config{}
	cfg.Git.DefaultBranch = "main"

	if err := WriteManaged(cfg, false, false, nil); err != nil {
		t.Fatalf("WriteManaged: %v", err)
	}

	gitconfig := filepath.Join(home, ".gitconfig")
	data, err := os.ReadFile(gitconfig)
	if err != nil {
		t.Fatalf("reading ~/.gitconfig: %v", err)
	}
	if !strings.Contains(string(data), shell.MarkerStart) {
		t.Error("~/.gitconfig missing marker block")
	}
	if !strings.Contains(string(data), "managed.gitconfig") {
		t.Error("~/.gitconfig missing include path")
	}
}

func TestWriteManaged_Idempotent(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	cfg := config.Config{}
	cfg.Git.DefaultBranch = "main"

	if err := WriteManaged(cfg, false, false, nil); err != nil {
		t.Fatalf("first WriteManaged: %v", err)
	}
	if err := WriteManaged(cfg, false, false, nil); err != nil {
		t.Fatalf("second WriteManaged: %v", err)
	}

	gitconfig := filepath.Join(home, ".gitconfig")
	data, err := os.ReadFile(gitconfig)
	if err != nil {
		t.Fatal(err)
	}
	// Marker should appear exactly once.
	count := strings.Count(string(data), shell.MarkerStart)
	if count != 1 {
		t.Errorf("expected 1 marker block, got %d", count)
	}
}

