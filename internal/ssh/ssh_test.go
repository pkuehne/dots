func TestGenerateConfig_Deterministic(t *testing.T) {
	cfg := config.Config{}
	cfg.SSH.Hosts = []config.SSHHost{
		{
			Host: "*.internal",
			Options: map[string]string{
				"user":          "peter",
				"port":          "2222",
				"identity_file": "~/.ssh/id_ed25519",
				"forward_agent": "yes",
			},
		},
	}

	first := GenerateConfig(cfg, []string{"linux"})
	for i := 0; i < 20; i++ {
		if got := GenerateConfig(cfg, []string{"linux"}); got != first {
			t.Fatalf("GenerateConfig output not deterministic:\n--- first ---\n%s\n--- got ---\n%s", first, got)
		}
	}
}

func TestWriteManaged_DryRun(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	cfg := config.Config{}
	if err := WriteManaged(cfg, []string{"linux"}, true); err != nil {
		t.Fatalf("WriteManaged dry-run: %v", err)
	}
	// No files should be created.
	if _, err := os.Stat(filepath.Join(home, ".config", "dots", "ssh", "config")); !os.IsNotExist(err) {
		t.Error("dry-run must not create ssh config file")
	}
}

