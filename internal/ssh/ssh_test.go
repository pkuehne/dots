func TestGenerateConfig_Basic(t *testing.T) {
	cfg := config.Config{}
	cfg.SSH.Hosts = []config.SSHHost{
		{
			Host:    "*.internal",
			Options: map[string]string{"user": "peter", "identity_file": "~/.ssh/id_ed25519"},
		},
	}
	result := GenerateConfig(cfg, []string{"linux"})
	if !strings.Contains(result, "Host *.internal") {
		t.Error("expected Host entry")
	}
	if !strings.Contains(result, "User peter") {
		t.Error("expected User option")
	}
	if !strings.Contains(result, "IdentityFile ~/.ssh/id_ed25519") {
		t.Error("expected IdentityFile option")
	}
}

func TestGenerateConfig_PlatformFilter(t *testing.T) {
	cfg := config.Config{}
	cfg.SSH.Hosts = []config.SSHHost{
		{
			Host:    "*.internal",
			Only:    []string{"linux", "darwin"},
			Options: map[string]string{"user": "peter"},
		},
	}

	result := GenerateConfig(cfg, []string{"termux"})
	if strings.Contains(result, "Host *.internal") {
		t.Error("host should be filtered for termux")
	}

	result = GenerateConfig(cfg, []string{"linux"})
	if !strings.Contains(result, "Host *.internal") {
		t.Error("host should appear for linux")
	}
}

