func TestSnakeToSSHKeyword_KnownKeys(t *testing.T) {
	cases := []struct {
		in, want string
	}{
		{"identity_file", "IdentityFile"},
		{"forward_agent", "ForwardAgent"},
		{"strict_host_key_checking", "StrictHostKeyChecking"},
		{"proxy_jump", "ProxyJump"},
		{"user", "User"},
		{"port", "Port"},
	}
	for _, tc := range cases {
		got := SnakeToSSHKeyword(tc.in)
		if got != tc.want {
			t.Errorf("SnakeToSSHKeyword(%q) = %q, want %q", tc.in, got, tc.want)
		}
	}
}

func TestSnakeToSSHKeyword_FallbackToPascalCase(t *testing.T) {
	got := SnakeToSSHKeyword("some_unknown_key")
	if got != "SomeUnknownKey" {
		t.Errorf("expected PascalCase fallback, got %q", got)
	}
}

