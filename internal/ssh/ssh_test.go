func TestWriteManaged_Idempotent(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	cfg := config.Config{}
	if err := WriteManaged(cfg, []string{"linux"}, false); err != nil {
		t.Fatal(err)
	}
	if err := WriteManaged(cfg, []string{"linux"}, false); err != nil {
		t.Fatal(err)
	}

	sshConfig := filepath.Join(home, ".ssh", "config")
	data, _ := os.ReadFile(sshConfig)
	count := strings.Count(string(data), sshIncludeLine)
	if count != 1 {
		t.Errorf("expected Include line once, got %d", count)
	}
}

func TestWriteManaged_PrependToExisting(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	sshDir := filepath.Join(home, ".ssh")
	if err := os.MkdirAll(sshDir, 0o700); err != nil {
		t.Fatal(err)
	}
	sshConfig := filepath.Join(sshDir, "config")
	existing := "Host existing\n    User me\n"
	if err := os.WriteFile(sshConfig, []byte(existing), 0o600); err != nil {
		t.Fatal(err)
	}

	cfg := config.Config{}
	if err := WriteManaged(cfg, []string{"linux"}, false); err != nil {
		t.Fatal(err)
	}

	data, _ := os.ReadFile(sshConfig)
	text := string(data)
	if !strings.HasPrefix(text, sshIncludeLine) {
		t.Error("Include line should be prepended")
	}
	if !strings.Contains(text, "Host existing") {
		t.Error("existing content should be preserved")
	}
}

