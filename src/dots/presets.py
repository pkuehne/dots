from __future__ import annotations

from dots.config import Config
from dots.constants import GENERATED_HEADER
from dots.utils import expand


def generate_fzf_preset(shell_name: str = "zsh") -> str:
    lines = [
        GENERATED_HEADER.replace("dots", "dots preset: fzf"),
        "# To customise: dots presets eject fzf",
        "",
        "command -v fzf >/dev/null 2>&1 || return",
        "",
        'export FZF_DEFAULT_OPTS="--height 40% --layout=reverse --border --cycle"',
        "",
        "# Use rg for file listing if available",
        "command -v rg >/dev/null 2>&1 && \\",
        "    export FZF_DEFAULT_COMMAND=\"rg --files --hidden --glob '!.git'\"",
        "",
        "# Shell key bindings — location varies by install method",
        "for _fzf_f in \\",
        f"    /usr/share/doc/fzf/examples/key-bindings.{shell_name}.sh \\",
        f"    /usr/share/fzf/key-bindings.{shell_name}.sh \\",
        f"    ~/.fzf.{shell_name}; do",
        '    [ -f "$_fzf_f" ] && source "$_fzf_f" && break',
        "done",
        "unset _fzf_f",
    ]
    return "\n".join(lines) + "\n"


