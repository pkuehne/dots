def generate_zprofile(config: Config) -> str:
    lines = [
        GENERATED_HEADER,
        "# Source: dots.toml [env] + [shell] path",
        "# Regenerate: dots apply",
        "# Login shell only: PATH and env, no interactive features.",
        "",
    ]
    # Env
    for key, value in sorted(config.env.items()):
        lines.append(f'export {key}="{value}"')
    lines.append("")
    # Path
    paths = list(config.shell.path)
    for tool in config.tools:
        for p in tool.shell.path:
            if p not in paths:
                paths.append(p)
    if config.tools:
        bin_dir = config.tools_config.bin_dir
        if bin_dir not in paths:
            paths.insert(0, bin_dir)
    for p in paths:
        expanded = expand(p)
        lines.append(
            f'case ":$PATH:" in *":{expanded}:"*) ;; *) export PATH="{expanded}:$PATH" ;; esac'
        )
    return "\n".join(lines) + "\n"


def generate_profile(config: Config) -> str:
    # POSIX sh compatible version
    return generate_zprofile(config)
