def test_ssh_config_multiple_hosts(dots):
    """Multiple SSH hosts rendered correctly."""
    config = dots.Config()
    config.ssh_hosts = [
        dots.SSHHost(host="host1", options={"user": "alice"}),
        dots.SSHHost(host="host2", options={"user": "bob", "port": 2222}),
    ]

    with patch.object(dots, "detect_platform", return_value="linux"):
        result = dots.generate_ssh_config(config)

    assert "Host host1" in result
    assert "Host host2" in result
    assert "User alice" in result
    assert "User bob" in result
    assert "Port 2222" in result
